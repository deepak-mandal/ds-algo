                    <h4>From Guwahati Airport :</h4>
                    <p>The distance from Lokpriya Gopinath Bordoloi International Airport - Guwahati (GAU) to IIT Guwahati is 
                        about 22 kms. In the Arrival Lounge (inside Airport), there is a pre-paid taxi counter. You can take 
                        a prepaid taxi from the airport to reach IIT Guwahati campus.</p>
                    <h4>From Guwahati Railway Station :</h4>
                    <p>The distance from Guwahati Railway Station (GHY) to IIT Guwahati is about 18 kms. If you are arriving
                        at Guwahati Railway Station by rail, come out through Platform No.1 exit gate (Panbazar or Lakhhtokia side)
                        . A limited number of IIT buses run regularly from Guwahati city - Lakhtokia (Near Don Bosco School's Church/
                        Cotton College Boys Hostel, which is about 0.5 km from the Guwahati Railway Station) to IIT Campus. or 
                        you may have to hire taxi or auto-rickshaws</p><br>
                    <h4>Where to Stay</h4>
                    <p>We recommend the participants to stay within the IIT Guwahati campus. Faculty participants, teachers and
                        industry participants may choose to stay at the Visitors Hostel (<a href="https://www.iitg.ac.in/stud/gymkhana/hostelBoard/hab.html" target="_blank">See link</a>).
